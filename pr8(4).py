import math

def is_prime(n):
    if n <= 1:
        return False
    if n <= 3:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    for i in range(5, int(math.sqrt(n)) + 1, 6):
        if n % i == 0 or n % (i + 2) == 0:
            return False
    return True

num = int(input("Enter a number : "))
if is_prime(num):
    print(f"{num} is prime.")
else:
    print(f"{num} is not prime.")